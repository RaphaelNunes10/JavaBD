package funcoes;



public class Dados {
    int txRG;
    String txNome, txEmail, txCelular, txTelefone;

    public int getTxRG() {
        return txRG;
    }

    public void setTxRG(int txRG) {
        this.txRG = txRG;
    }

    public String getTxNome() {
        return txNome;
    }

    public void setTxNome(String txNome) {
        this.txNome = txNome;
    }

    public String getTxEmail() {
        return txEmail;
    }

    public void setTxEmail(String txEmail) {
        this.txEmail = txEmail;
    }

    public String getTxCelular() {
        return txCelular;
    }

    public void setTxCelular(String txCelular) {
        this.txCelular = txCelular;
    }

    public String getTxTelefone() {
        return txTelefone;
    }

    public void setTxTelefone(String txTelefone) {
        this.txTelefone = txTelefone;
    }
}
